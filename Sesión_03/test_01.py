"""Listas en Python"""

"""Listas: pop(): Quitar un elemento de una lista"""

paises = ["Perú", "Cánada", "Argentina", "México", "España"]

print("Mi lista inicial: {}". format(paises))

paises.pop()
print("Mi lista actualizada es: {}". format(paises))

print("paises[2]: {}".format(paises[2]))
paises.pop(2)
print("Mi lista actualizada es: {}". format(paises))

paises.pop(2)
print("Mi lista actualizada es: {}". format(paises))